if (ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u') {
    console.log("".concat(ch, "  is a Vowel"));
}
else if (ch == 'A' || ch == 'E' || ch == 'I' || ch == 'O' || ch == 'U') {
    console.log("".concat(ch, " is a Vowel"));
}
else {
    console.log("".concat(ch, " is a consonant"));
}
